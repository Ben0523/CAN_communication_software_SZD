/******************************************************************************
* Function:         void USART0_Init (unsigned int baud)
* Description:      USART0 port inicializalasa
* Input:			unsigned int baud
* Output:
* Notes:
******************************************************************************/
void USART0_Init(unsigned int baud)
{
	 UBRR0H = (unsigned char) (baud>>8);
	 UBRR0L = (unsigned char) baud;
	 UCSR0C = (0<<UMSEL0) | (0<<UPM0) | (0<<USBS0) | (3<<UCSZ0);
	 UCSR0B =  (1<<RXCIE0) | (1<<RXEN0) | (1<<TXEN0); //interrupt
}
/******************************************************************************
* Function:         void USART0_Transmit (unsigned char data)
* Description:      USART0 porton valo uzenet kuldes
* Input:			unsigned char data
* Output:
* Notes:
******************************************************************************/
void USART0_Transmit (unsigned char data)
{
	 while ( ! ( UCSR0A & (1<<UDRE0)));
	 UDR0 = data;
}
/******************************************************************************
* Function:         unsigned char USART0_Receive (void)
* Description:      USART0 porton valo uzenet fogadas
* Input:
* Output:			unsigned char UDR0
* Notes:
******************************************************************************/
unsigned char USART0_Receive (void)
{
	 while ( ! (UCSR0A & (1<<RXC0)));
	 return UDR0;
}
